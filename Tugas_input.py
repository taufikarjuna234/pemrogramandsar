#Luas Bangun Trapesium
a = int(input("masukkan nilai a:")) 
b = int(input("masukkan nilai b:")) 
t = int(input("masukkan nilai t"))
rumus = 1/2*(a+b)*t
print("---------------------------------")
print("Perhitungan Luas Trapesium")
print("Besar Nilai a:",a)
print("Besar Nilai b:",b)
print("Besar Nilai t:",t)
print("Nilai Luas Trapesium:",rumus)
print("---------------------------------")


