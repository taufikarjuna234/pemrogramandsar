#Perhitungan (a+b)(a-c) 
a = int(input("masukkan nilai a:")) 
b = int(input("masukkan nilai b:")) 
c = int(input("masukkan nilai c"))
rumus = (a+b)*(a-c)
print("---------------------------------")
print("Perhitungan (a+b)(a-c)")
print("Besar Nilai a:",a)
print("Besar Nilai b:",b)
print("Besar Nilai c:",c)
print("Hasil yang didapat adalah:",rumus)
print("---------------------------------")


